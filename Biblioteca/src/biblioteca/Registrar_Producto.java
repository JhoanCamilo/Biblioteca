    public void llenarComboBox(){
        String[] Libros = {"Novela", "Poesia"};
        String[] Periodicos = {"Magazine", "Revista"};
        String[] Multimedia = {"Documental", "Reportaje"};
        Tipo.removeAllItems();

        if (Tipo_Producto.getSelectedItem().equals("Libro")) {
            for (String librito : Libros) {
                Tipo.addItem(librito);
            }
            Titulo.setEnabled(true);
            Autor.setEnabled(true);
            Nacionalidad.setEnabled(true);
            Birthday.setEnabled(true);
        }
        else if (Tipo_Producto.getSelectedItem().equals("Periodico")) {
            for (String periodic : Periodicos) {
                Tipo.addItem(periodic);
            }
            Titulo.setEnabled(false);
            Autor.setEnabled(false);
            Nacionalidad.setEnabled(false);
            Birthday.setEnabled(false);
        }
        else if (Tipo_Producto.getSelectedItem().equals("Multimedia")) {
            for (String multi : Multimedia) {
                Tipo.addItem(multi);
            }
            Titulo.setEnabled(true);
            Autor.setEnabled(true);
            Nacionalidad.setEnabled(true);
            Birthday.setEnabled(true);
        }
    }

