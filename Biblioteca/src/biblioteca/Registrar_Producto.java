    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        String Nombre = Titulo.getText();
        String FechaPublicacion = Fecha_Publicacion.getText();
        String Editor = Editorial.getText();
        String Escritor = Autor.getText();
        String PaisEscritor = Nacionalidad.getText();
        String fechaNacimiento = Birthday.getText();
        String Producto = (String) Tipo.getSelectedItem();
        ConexionDDBB resgistrar = new ConexionDDBB();

        if (Tipo_Producto.getSelectedItem().equals("Libro")) {
            resgistrar.RegistrarLibro(Nombre, FechaPublicacion, Editor, Escritor, Producto);
            resgistrar.RegistrarAutores(Escritor, PaisEscritor, fechaNacimiento);
            Titulo.setText("");
            Fecha_Publicacion.setText("");
            Editorial.setText("");
            Autor.setText("");
            Nacionalidad.setText("");
            Birthday.setText("");
        }
        else if (Tipo_Producto.getSelectedItem().equals("Periodico")) {
            resgistrar.RegistrarPerdiodico(FechaPublicacion, Editor, Producto);
            Fecha_Publicacion.setText("");
            Editorial.setText("");
        }
        else if (Tipo_Producto.getSelectedItem().equals("Multimedia")) {
            resgistrar.RegistrarMultimedia(Nombre, FechaPublicacion, Editor, Escritor, Producto);
        }
    }//GEN-LAST:event_RegistrarActionPerformed

